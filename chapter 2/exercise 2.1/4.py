# Q. What if you put a period at the end of a statement?
a = 55.

print(a)
print(type(a))
#output : 55.0
#output : <class 'float>

#putting a period . at the end of a statement does not cause an error in python
#however it can change the behaviour of the statement which is shown in the above eample
# the integer 55 after adding a period . at the end, it changes its data type to float.